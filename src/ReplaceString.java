public class ReplaceString {
    public static void main(String[] args) {
        String changeString = "블랙핑크";

         System.out.println("원본 문자열: " + changeString);
         System.out.println("반대 문자열: "+
                 changeString.charAt(3)+
                 changeString.charAt(2)+
                 changeString.charAt(1)+
                 changeString.charAt(0));
    }
}
